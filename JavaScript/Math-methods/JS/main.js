// Math methods and Properties

// it displays the value of PI
console.log(Math.PI)

// Math.trunc() -> it removes all the decimal numbers
console.log("Trunc: ", Math.trunc(Math.PI))

// Math.round() -> it rounds the number according to the basic logic, from up to 5
console.log("Round: ", Math.round(3.5))

// Math.ceil() -> it always rounds up no matter the decimal
console.log("Ceil: ", Math.ceil(Math.PI))

// Math.floor() -> it always rounds down no matter the decimal
console.log("Floor: ", Math.floor(Math.PI))

// Math.pow() -> it takes two arguments, the base number and the exponent
console.log("Pow (a number to the power of another number): ", Math.pow(2, 4))

// Math.min() -> returns the smallest number from zero or more numbers
console.log("Min: ", Math.min(2, 0.5, 9))

// Math.max() -> returns the largest number from zero or more numbers
console.log("Max: ", Math.max(2, 0.5, 9))

// Math.random() -> it provides a pseudo-random number from 0 to 1, it includes 0 but not 1
console.log("Random: ", Math.random(), ", ", Math.random())
// From 1 to 10 -> the one should be added to include the 10
console.log("Random + floor: ", Math.floor(Math.random() * 10) + 1)
console.log("Random + floor: ", Math.floor(Math.random() * 10) + 1)
